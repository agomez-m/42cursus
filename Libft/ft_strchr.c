#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}

/* localiza la primera aparicion 
de c (convertido a char)
en la cadena apuntada por s.
La función devuelve un puntero al carácter encontrado,
 o NULL si no se encuentra. */